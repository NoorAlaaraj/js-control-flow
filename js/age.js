let age = 15

if (age == 0) {
  console.log('infant')
}

else if (age <= 1) {
  console.log('toddler')
}

else if (age <= 3) {
  console.log('child')
}

else if (age <= 13) {
  console.log('preteen')
}

else if (age <= 18) {
  console.log('teen')
}

else if (age <= 20) {
  console.log('young adult')
}

